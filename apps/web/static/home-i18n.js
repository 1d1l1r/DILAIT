export function normalizeLocale(locale) {
  return LOCALES.includes(locale) ? locale : "ru";
}

export function chromeCopy(locale) {
  return CHROME[normalizeLocale(locale)];
}

export function dayLabel(locale, day) {
  return DAYS[normalizeLocale(locale)]?.[day] || `[[days.${day}]]`;
}

export function legacyText(locale, ru, en) {
  const normalized = normalizeLocale(locale);
  if (normalized === "ru") return ru;
  if (normalized === "en") return en;
  return KK[en] || kkPattern(en) || `[[${en}]]`;
}

function kkPattern(value) {
  let match = value.match(/^After (.+)s -> (.+)$/);
  if (match) return `${match[1]} с кейін -> ${match[2]}`;
  match = value.match(/^(.+) at (.+) -> (.+)$/);
  if (match) return `${match[1]} ${match[2]} уақытта -> ${match[3]}`;
  match = value.match(/^(.+) (sunset|sunrise) \((.+) min\) -> (.+)$/);
  if (match) return `${match[1]} ${match[2] === "sunset" ? KK.Sunset : KK.Sunrise} (${match[3]} мин) -> ${match[4]}`;
  match = value.match(/^(\d+) light\(s\)$/);
  if (match) return `${match[1]} жарық`;
  match = value.match(/^(\d+) on$/);
  if (match) return `${match[1]} қосулы`;
  match = value.match(/^(\d+) member\(s\)$/);
  if (match) return `${match[1]} қатысушы`;
  match = value.match(/^(\d+) action\(s\)$/);
  if (match) return `${match[1]} әрекет`;
  match = value.match(/^(\d+) member\(s\) across (\d+) family group\(s\)\.$/);
  if (match) return `${match[1]} қатысушы, ${match[2]} құрылғы түрі.`;
  match = value.match(/^(\d+) action\(s\) ready to run\.$/);
  if (match) return `${match[1]} әрекет іске қосуға дайын.`;
  match = value.match(/^Likely (.+) light nearby$/);
  if (match) return `Жақын жерде ${match[1]} жарығы болуы мүмкін`;
  match = value.match(/^(.+) is already onboarded$/);
  if (match) return `${match[1]} бұрын қосылған`;
  match = value.match(/^Showing only lights from (.+)\.$/);
  if (match) return `Тек ${match[1]} бөлмесіндегі жарық көрсетіледі.`;
  match = value.match(/^(.+) in (.+)\.$/);
  if (match) return `${match[1]} — ${match[2]}.`;
  match = value.match(/^Step (.+)$/);
  if (match) return `${match[1]}-қадам`;
  match = value.match(/^Delete (room|light|group|scene) "(.+)"\?$/);
  if (match) return `"${match[2]}" жою?`;
  return null;
}
